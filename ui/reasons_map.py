# UI-only: человекочитаемые формулировки причин
# Контракты и ключи НЕ меняются

REASON_MAP = {
    # режимы
    "MODE_MANUAL_ONLY": "Ручной режим: торговля заблокирована",

    # стопы
    "MANUAL_STOP": "Ручной стоп активирован",
    "HARD_STOP": "Аварийный стоп системы",

    # возможные расширения
    # "AUTO_DISABLED": "Автономный режим отключён",
}

def human_reason(code: str) -> str:
    """
    Вернуть человекочитаемое объяснение причины.
    Fallback — сам код (контракт сохраняется).
    """
    return REASON_MAP.get(code, code)
